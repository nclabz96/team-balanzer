type NavItem = { href: string; label: string; icon: React.ReactNode; exact?: boolean }

export default function BottomNav() {
  const { user, isLoading } = useAuth()
  const pathname = usePathname()

  if (isLoading) return null

  const items: NavItem[] = [
    { href: '/', label: 'Home', icon: <HomeIcon />, exact: true },
    ...(user
      ? [
          { href: '/session/new', label: 'New Session', icon: <PlusCircleIcon /> },
          { href: '/admin',       label: 'Players',     icon: <UsersIcon /> },
          { href: '/admin/settings', label: 'Settings', icon: <CogIcon /> },
        ]
      : [{ href: '/login', label: 'Admin', icon: <LockIcon /> }]
    ),
  ]

  return (
    <nav className="fixed bottom-0 inset-x-0 bg-white border-t border-gray-200 sm:hidden z-40">
      <div className="flex items-center justify-around">
        {items.map(item => {
          const active = item.exact
            ? pathname === item.href
            : item.href === '/admin'
              ? pathname === '/admin'
              : pathname.startsWith(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center gap-0.5 flex-1 py-2.5 transition-colors ${
                active ? 'text-green-700' : 'text-gray-400 hover:text-gray-600'
              }`}
            >
              {item.icon}
              <span className="text-[10px] font-medium leading-none">{item.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
